package banco;

/**
 *
 * @author SUP08
 */
public class Cliente {

    public enum tipo {
        FÍSICA,
        JURÍDICA
    }
    private String nome;
    private String cpf;
    private String cnpj;

    public Cliente() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

}
